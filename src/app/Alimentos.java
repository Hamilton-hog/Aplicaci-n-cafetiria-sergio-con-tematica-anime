    private void TxtINFORMEMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TxtINFORMEMouseClicked
        if(TxtINFORME.getText().equals("INFORME")){
            TxtINFORME.setText("CERRAR");
            jTableProductos.setVisible(false);
            jScrollPane1.setVisible(false);
            Jpanelinforme.setVisible(true);
            jPanelINFORME.setBackground(new Color(255,255,0));
            SetImageLabel(ImgEstees,"src/imagenes/pr4.png");
            SetImageLabel(Imgborde,"src/imagenes/borde.png");
            SetImageLabel(Imgborde1,"src/imagenes/borde.png");
            SetImageLabel(Imgborde2,"src/imagenes/borde.png");
            SetImageLabel(Imgborde3,"src/imagenes/borde.png");
            try{
                ps = con.prepareStatement("SELECT "+FNombre+" AS Mayor FROM "+FTabla+" WHERE "+FPrecio+" = (SELECT MAX("+FPrecio+") FROM "+FTabla+")");
                rs = ps.executeQuery();

                while(rs.next()){
                    TxtMaximo.setText(rs.getString("Mayor"));

                }
                ps = con.prepareStatement("SELECT "+FNombre+" AS Menor FROM "+FTabla+" WHERE "+FPrecio+" = (SELECT MIN("+FPrecio+") FROM "+FTabla+")");
                rs = ps.executeQuery();

                while(rs.next()){
                    TxtMinimo.setText(rs.getString("Menor"));
                }
                ps = con.prepareStatement("SELECT AVG("+FPrecio+") AS promedio,SUM("+FPrecio+"*"+FInventario+") AS total_inventario FROM "+FTabla+"");
                rs = ps.executeQuery();

                while(rs.next()){
                    TxtTPromedio.setText(rs.getString("promedio"));
                    TxtTotal.setText(rs.getString("total_inventario"));
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Tabla solicitada no existente, dirijete a OP/TablaSet."); 
            }              
        }else if(TxtINFORME.getText().equals("CERRAR")){
            TxtINFORME.setText("INFORME");
            Jpanelinforme.setVisible(false);
            jTableProductos.setVisible(true);
            jScrollPane1.setVisible(true);
            jPanelINFORME.setBackground(new Color(64,176,218));
            
        }
    }//GEN-LAST:event_TxtINFORMEMouseClicked

