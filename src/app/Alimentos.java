    private void limpiar(){
        FieldINVENTARIO.setText("Inventario");
        FieldINVENTARIO.setForeground(Color.gray);
        FieldPRECIO.setText("Precio");
        FieldPRECIO.setForeground(Color.gray);
        FieldNombre.setText("Nombre");
        FieldNombre.setForeground(Color.gray);
        TxtNUM.setText("NUM");
        loadTabla();
    }
    private void loadTabla(){
        DefaultTableModel modeloTabla = (DefaultTableModel) jTableProductos.getModel();
        modeloTabla.setRowCount(0);
        
        try {
            ps = con.prepareStatement("SELECT * FROM "+FTabla+"");
            rs = ps.executeQuery();
            Object[] cliente = new Object[4];
            while(rs.next()){
                cliente [0] = rs.getInt(FClave);
                cliente [1] = rs.getString(FNombre);
                cliente [2] = rs.getDouble(FPrecio);
                cliente [3] = rs.getInt(FInventario);
                
                modeloTabla.addRow(cliente);
            }
            jTableProductos.setModel(modeloTabla);
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Tabla solicitada no existente, dirijete a OP/TablaSet."); 
        }
    }
